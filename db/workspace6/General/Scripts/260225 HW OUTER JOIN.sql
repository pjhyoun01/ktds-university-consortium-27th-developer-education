-- 1. 지역별 부서의 수를 조회한다.
-- (부서가 없으면 부서의 수는 0으로 조회한다.)
SELECT L.CITY
	 , COUNT(D.DEPARTMENT_ID) DEPT_CNT
  FROM LOCATIONS L
  LEFT OUTER JOIN DEPARTMENTS D
    ON L.LOCATION_ID = D.LOCATION_ID
 GROUP BY L.CITY
 ORDER BY L.CITY
;
-- 2. 지역별 사원의 평균월급을 조회한다. 
-- (사원이 없으면 평균월급은 0으로 조회한다.)
SELECT L.CITY
	 , ROUND(NVL(AVG(E.SALARY), 0)) AVG_SAL
  FROM LOCATIONS L
  LEFT OUTER JOIN DEPARTMENTS D
    ON L.LOCATION_ID = D.LOCATION_ID
  LEFT OUTER JOIN EMPLOYEES E
    ON D.DEPARTMENT_ID = E.DEPARTMENT_ID
 GROUP BY L.CITY
 ORDER BY L.CITY
;
-- 3. 지역별 사원의 수를 도시명으로 오름차순 정렬하여 조회한다.
-- (사원이 없으면 사원의 수는 0으로 조회한다.)
SELECT L.CITY
	 , COUNT(E.EMPLOYEE_ID) EMP_CNT
  FROM LOCATIONS L
  LEFT OUTER JOIN DEPARTMENTS D
    ON L.LOCATION_ID = D.LOCATION_ID
  LEFT OUTER JOIN EMPLOYEES E
    ON D.DEPARTMENT_ID = E.DEPARTMENT_ID
 GROUP BY L.CITY
 ORDER BY L.CITY
;
-- 4. 모든 사원들의 현재 직무명과 과거의 직무명을 조회한다. 
-- 만약 직무가 한번도 변경되지 않았다면, 과거의 직무명은 '없음' 으로 조회한다.
SELECT E.EMPLOYEE_ID
	 , J.JOB_TITLE NOW_TITLE
	 , CASE NVL(J2.JOB_TITLE, '0')
		 WHEN '0' THEN '없음'
		 ELSE J2.JOB_TITLE 
	   END POST_TITLE
  FROM EMPLOYEES E
  LEFT JOIN JOBS J
    ON E.JOB_ID = J.JOB_ID
  LEFT OUTER JOIN JOB_HISTORY JH
    ON E.EMPLOYEE_ID = JH.EMPLOYEE_ID
  LEFT JOIN JOBS J2
 	ON JH.JOB_ID = J2.JOB_ID
 ORDER BY E.EMPLOYEE_ID 
;
